public class ForEachLoop {
    public static void main (String args[]) {

        String names[]={"şammas","ayşe","mesud"};

        //foreach
        for (String i:names) {
            System.out.println(i);

        }


        for (int i=0;i<3;i++){
            System.out.println(names[i]);

        }

    }
}
